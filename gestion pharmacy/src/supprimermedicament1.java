    private boolean supprimerMedicament(int codeMedicament) {
    try {
        // Établir une connexion à la base de données
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/pharmacie", "root", "");
        
        // Préparer la requête SQL pour supprimer le médicament
        String sql = "DELETE FROM medicament WHERE code_medic = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        
        stmt.setInt(1, codeMedicament);
        

        int rowsAffected = stmt.executeUpdate();
        
        stmt.close();
        conn.close();
        
        return rowsAffected > 0;
    } catch (SQLException e) {
        e.printStackTrace();
        return false;
    }
}

