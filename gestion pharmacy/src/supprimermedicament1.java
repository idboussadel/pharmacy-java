    private void btnsupprimermedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsupprimermedActionPerformed

        String codeMedicamentStr = inputsupprimermed.getText();

    if (codeMedicamentStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez saisir un code de médicament.", "Erreur", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        int codeMedicament = Integer.parseInt(codeMedicamentStr);
        boolean suppressionReussie = supprimerMedicament(codeMedicament);

        if (suppressionReussie) {
            JOptionPane.showMessageDialog(this, "Le médicament a été supprimé avec succès.", "Succès", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Le médicament que vous voulez supprimer n'existe pas.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Veuillez saisir un code de médicament valide.", "Erreur", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnsupprimermedActionPerformed

